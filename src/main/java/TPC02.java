package main.java;

public class TPC02 {

    public static void main(String[] args) {
        int a, b, c;
        a = 1;
        b = 1;
        c = a + b;
        System.out.println("c = " + c);

        float f;
        f = 34.5f;
        System.out.println("f = " + f);

        char d;
        d = 'A';
        System.out.println("d = " + d);

        boolean g;
        g = true;

        System.out.println("g = " + g);
    }
}
